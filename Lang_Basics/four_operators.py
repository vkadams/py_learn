# Arithmatic operators
a=50
b=20
print(a+b)
print(a-b)
print(a*b)
print(a//b) # 2 -It divides \(a\) by \(b\) and rounds the result down to the nearest whole number
print(a/b) # 2.5
print(a%b) # 10 -reminder
print(a**b) # 9536743164062500000000000000000000 exponential

# Relational operators
print(a > b) # True
print(a < b) # False
print(a == b) # False
print(a != b) # True
print(a <= b) # False
print(a >= b) # True

# Logical Operators - and, or, not
m = True
n = False
print(m and n) # False
print(m or n) # True
print(not m) # False
print(not n) # True

print((m and n) and (m or n)) # False
print((m and n) or (m or n)) # True
